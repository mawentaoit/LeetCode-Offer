/**
 * 位运算规律总结
 * 1 与运算
 * 对于某位为1的bit_mask，如果与1得1，否则得0
 * if(a&1 == 0) 偶数
 * if(a&1 == 1) 奇数
 * (n-1)&n 消去n最后一位的1，可用于统计n比特位上1的个数
 * ~(n-1) & n //仅保留n最后一位的1，可用于生成相关mask （-n = ~(n-1)）
 * 2 异或运算
 * a ^ a = 0; 相同数异或得0
 * a ^ b ^ a = b; 
 * 不使用新的变量，交换两个变量的值
 * a = a + b;
 * b = a - b;
 * a = a - b;
 * 3 取反运算
 * -1的补码全为1
 * x + ~x = -1
 */